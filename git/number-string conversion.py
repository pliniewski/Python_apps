'''The number-string conversion'''
itg = input("Enter integer number: ")
flt = input("Enter float number: ")
si = str(itg)
sf = str(flt)

print("Sum of strings: ")
print(si + ' ' + sf)




'''The string-number conversion'''


itg = int(si)
flt = float(sf)

print("Sum of integer and float numbers: ")
print(itg + flt)


